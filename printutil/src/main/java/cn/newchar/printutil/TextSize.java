public interface TextSize {
    /**
     * 一倍宽, 一倍高(标准文字大小)
     */
    int TEXT_WH_1X1 = 0;
    /**
     * 一倍宽, 二倍高
     */
    int TEXT_WH_1x2 = 1;
    /**
     * 两倍高一倍宽
     */
    int TEXT_WH_2x1 = 2;
    /**
     * 两倍宽两倍高
     */
    int TEXT_WH_2x2 = 3;
}
